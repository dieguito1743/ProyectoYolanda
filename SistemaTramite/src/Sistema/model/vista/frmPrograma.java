    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        Programa = txtPrograma.getText();
        Descripcion = txtDescripcion.getText();
        estado= cboestado.getSelectedItem().toString();
        Fecha = txtFecha.getText();
        
        oPrograma = new Programa();
        switch (opt) {
            case 'N':
            oPrograma.setPrograma(Programa);
            oPrograma.setDescripcion(Descripcion);
            oPrograma.setEstado(estado);
             oPrograma.setFecha(Fecha);

            try {
                if (ProgramaBo.grabarPrograma(oPrograma)) {
                    JOptionPane.showMessageDialog(this, "Se Registró Correctamente", "MENSAJE --> PROGRAMA", JOptionPane.INFORMATION_MESSAGE);
                    cargarDatos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo Registrar", "MENSAJE --> PROGRAMA", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "MENSAJE --> PROGRAMA", JOptionPane.ERROR_MESSAGE);
            }
            break;
            case 'M':
           Codigo = Integer.parseInt(txtcodigo.getText());
            oPrograma.setIdPrograma(Codigo);
            oPrograma.setPrograma(Programa);
            oPrograma.setDescripcion(Descripcion);
            oPrograma.setEstado(estado);
            oPrograma.setFecha(Fecha);

            try {
                if (ProgramaBo.modificarPrograma(oPrograma)) {
                    JOptionPane.showMessageDialog(this, "Se Modificó Correctamente", "PROGRAMA", JOptionPane.INFORMATION_MESSAGE);
                    cargarDatos();
                } else {
                    JOptionPane.showMessageDialog(this, "No se pudo Modificar", "PROGRAMA", JOptionPane.ERROR_MESSAGE);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "PROGRAMA", JOptionPane.ERROR_MESSAGE);
            }
            break;
        }
        camposDeshabilitado();
        lblcodigo.setVisible(false);
        txtcodigo.setVisible(false);
        cargarDatos();
        btnsConfirmarDeshabilitar();
        btnsEdicionHabilitar();
    }//GEN-LAST:event_btngrabarActionPerformed

