        private void cargarDatos() {
            try {
                TableColumn columna;
                DefaultTableModel ProgramaTableModel = ProgramaBo.obtenerListaPrograma();
                tblPrograma.setModel(ProgramaTableModel);
                setAnchoColumnas();
                columna = tblPrograma.getColumnModel().getColumn(0);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

        private void cargarDatosBuscar() {
            try {
                TableColumn columna;
                DefaultTableModel ProgramaTableModel = ProgramaBo.obtenerListaPrograma(txtPrograma.getText());
                tblPrograma.setModel(ProgramaTableModel);
                setAnchoColumnas();
                columna = tblPrograma.getColumnModel().getColumn(0);
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        public void setAnchoColumnas() {
            JViewport scroll = (JViewport) tblPrograma.getParent();
            int ancho = scroll.getWidth();
            int anchoColumna = 0;
            TableColumnModel modeloColumna = tblPrograma.getColumnModel();
            TableColumn columnaTabla;
            for (int i = 0; i < tblPrograma.getColumnCount(); i++) {
                columnaTabla = modeloColumna.getColumn(i);
                switch (i) {
                    case 0:
                    anchoColumna = (4 * ancho) / 100;
                    break;
                    case 1:
                    anchoColumna = (6 * ancho) / 100;
                    break;
                    case 2 :
                    anchoColumna = (6 * ancho) / 100;
                    break;
                    case 3:
                    anchoColumna = (6 * ancho) / 100;
                     break;
                    case 4:
                    anchoColumna = (6 * ancho) / 100;

                }
                columnaTabla.setPreferredWidth(anchoColumna);
            }
        }

