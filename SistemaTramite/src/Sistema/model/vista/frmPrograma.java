    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        if (Codigo != 0) {
            opt = 'E';
            lblcodigo.setVisible(true);
            txtcodigo.setVisible(true);
            txtcodigo.setText("" + Codigo);
            txtPrograma.setText(Programa);
            txtDescripcion.setText(Descripcion);
            cboestado.getSelectedItem();
            try {
                Codigo = Integer.parseInt(txtcodigo.getText());

                if (opt == 'E') {
                    oPrograma = new Programa();
                    oPrograma.setIdPrograma(Codigo);
                    int rpta = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar",
                        "PROGRAMA", JOptionPane.YES_NO_OPTION);
                    if (rpta == 0) {
                        if (ProgramaBo.eliminarPrograma(oPrograma)) {
                            JOptionPane.showMessageDialog(this, "Se Eliminó Correctamente", "PROGRAMA",
                                JOptionPane.INFORMATION_MESSAGE);
                            cargarDatos();
                        } else {
                            JOptionPane.showMessageDialog(this, "No se pudo Eliminar", "PROGRAMA",
                                JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "PROGRAMA", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione el Remitente", "PROGRAMA", JOptionPane.ERROR_MESSAGE);
        }
        btnsEdicionHabilitar();
        btnsConfirmarDeshabilitar();
    }//GEN-LAST:event_btneliminarActionPerformed

