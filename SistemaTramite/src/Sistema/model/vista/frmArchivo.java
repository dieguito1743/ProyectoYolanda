    private void cargarDatos() {
        try {
            TableColumn columna;
            DefaultTableModel ArchivoTableModel = ArchivoBo.obtenerListaArchivo();
            tblArchivo.setModel(ArchivoTableModel);
            setAnchoColumnas();
            columna = tblArchivo.getColumnModel().getColumn(0);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void cargarDatosBuscar() {
        try {
            TableColumn columna;
            DefaultTableModel ArchivoTableModel = ArchivoBo.obtenerListaArchivoBuscar(txtnombre.getText());
            tblArchivo.setModel(ArchivoTableModel);
            setAnchoColumnas();
            columna = tblArchivo.getColumnModel().getColumn(0);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }    
    public void setAnchoColumnas() {
        JViewport scroll = (JViewport) tblArchivo.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tblArchivo.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tblArchivo.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (4 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (6 * ancho) / 100;
                    break;
                case 2:
                    anchoColumna = (10 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
        }
    
    }//GEN-LAST:event_btncancelarActionPerformed
