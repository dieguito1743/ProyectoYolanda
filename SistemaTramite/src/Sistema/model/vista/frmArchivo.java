    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        Nombre = txtnombre.getText();
        
        oArchivo = new Archivo();
        switch (opt) {
            case 'N':
                oArchivo.setArchivo(Nombre);
                try {
                    if (ArchivoBo.grabarArchivo(oArchivo)) {
                        JOptionPane.showMessageDialog(this, "Se RegistrÃ³ Correctamente", "MENSAJE --> ARCHIVO", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Registrar", "MENSAJE --> ARCHIVO", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "MENSAJE --> ARCHIVO", JOptionPane.ERROR_MESSAGE);
                }
                break;
            case 'M':
                codigo = Integer.parseInt(txtcodigo.getText());
                oArchivo.setIdArchivo(codigo);
                oArchivo.setArchivo(Nombre);
                
                try {
                    if (ArchivoBo.modificarArchivo(oArchivo)) {
                        JOptionPane.showMessageDialog(this, "Se ModificÃ³ Correctamente", "ARCHIVO", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Modificar", "ARCHIVO", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "ARCHIVOO", JOptionPane.ERROR_MESSAGE);
                }
                break;
        }
        camposDeshabilitado();
        lblcodigo.setVisible(false);
        txtcodigo.setVisible(false);
        cargarDatos();
        btnsConfirmarDeshabilitar();
        btnsEdicionHabilitar();
        
    }//GEN-LAST:event_btngrabarActionPerformed

