    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        if (codigo != 0) {
            opt = 'E';
            lblcod.setVisible(true);
            txtcodigo.setVisible(true);
            txtcodigo.setText("" + codigo);
            txtRazonSocial.setText(RazonSocial);
            txtRUCDNI.setText(RUCDNI);
            txttelefono.setText(Telefono);
            txtemail.setText(email);
            try {
                int index = tblEmpleado.getSelectedRow();
                codigo = Integer.parseInt(tblEmpleado.getValueAt(index, 0).toString());
                if (opt == 'E') {
                    oEmpleado = new Empleado();
                    oEmpleado.setIdEmpleado(codigo);
                    int rpta = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar", "Empleado", JOptionPane.YES_NO_OPTION);
                    if (rpta == 0) {
                        if (EmpleadoBo.eliminarEmpleado(oEmpleado)) {
                            JOptionPane.showMessageDialog(this, "Se Eliminó Correctamente", "EMPLEADO",
                                    JOptionPane.INFORMATION_MESSAGE);
                            cargarDatos();
                        } else {
                            JOptionPane.showMessageDialog(this, "No se pudo Eliminar", "EMPLEADO",
                                    JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "EMPLEADO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione el EMPLEADO", "EMPLEADO", JOptionPane.ERROR_MESSAGE);
        }
        btnsEdicionHabilitar();
        btnsConfirmarDeshabilitar();

    }//GEN-LAST:event_btneliminarActionPerformed

