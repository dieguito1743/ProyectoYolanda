    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        RazonSocial = txtRazonSocial.getText();
        RUCDNI = txtRUCDNI.getText();
        Telefono = txttelefono.getText();
        email = txtemail.getText();
        if (chkestado.isSelected()) {
            estado = 0;
        } else {
            estado = 1;
        }
        oEmpleado = new Empleado();
        switch (opt) {
            case 'N':
                oEmpleado.setRazonSocial(RazonSocial);
                oEmpleado.setRUCDNI(RUCDNI);
                oEmpleado.setTelefono(Telefono);
                oEmpleado.setCorreo(email);
                oEmpleado.setEstado(estado);
                try {
                    if (EmpleadoBo.grabarEmpleado(oEmpleado)) {
                        JOptionPane.showMessageDialog(this, "Se Registró Correctamente", "MENSAJE --> EMPLEADO", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Registrar", "MENSAJE --> EMPLEADO", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "MENSAJE --> EMPLEADO", JOptionPane.ERROR_MESSAGE);
                }
                break;
            case 'M':
                codigo = Integer.parseInt(txtcodigo.getText());
                oEmpleado.setIdEmpleado(codigo);
                oEmpleado.setRazonSocial(RazonSocial);
                oEmpleado.setRUCDNI(RUCDNI);
                oEmpleado.setTelefono(Telefono);
                oEmpleado.setCorreo(email);
                oEmpleado.setEstado(estado);

                try {
                    if (EmpleadoBo.modificarEmpleado(oEmpleado)) {
                        JOptionPane.showMessageDialog(this, "Se Modificó Correctamente", "EMPLEADO", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Modificar", "EMPLEADO", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "EMPLEADO", JOptionPane.ERROR_MESSAGE);
                }
                break;
        }
        camposDeshabilitado();
        lblcod.setVisible(false);
        txtcodigo.setVisible(false);
        cargarDatos();
        btnsConfirmarDeshabilitar();
        btnsEdicionHabilitar();


    }//GEN-LAST:event_btngrabarActionPerformed

    private void cargarDatos() {
        try {
            TableColumn columna;
            DefaultTableModel EmpleadoTableModel = EmpleadoBo.obtenerListaEmpleado();
            tblEmpleado.setModel(EmpleadoTableModel);
            setAnchoColumnas();
            columna = tblEmpleado.getColumnModel().getColumn(0);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public boolean isCellEditable(int rowIndex, int vColIndex) {
        return false;
    }

    private void cargarDatosBuscar() {
        try {
            TableColumn columna;
            DefaultTableModel EmpleadoTableModel = EmpleadoBo.obtenerListaEmpleadoBuscar(txtRazonSocial.getText());
            tblEmpleado.setModel(EmpleadoTableModel);
            setAnchoColumnas();
            columna = tblEmpleado.getColumnModel().getColumn(0);
            int index = tblEmpleado.getSelectedRow();
            int indexc = tblEmpleado.getSelectedColumn();
            isCellEditable(index, indexc);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void setAnchoColumnas() {
        JViewport scroll = (JViewport) tblEmpleado.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tblEmpleado.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tblEmpleado.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (4 * ancho) / 100;
                    isCellEditable(tblEmpleado.getSelectedRow(), 0);
                    break;
                case 1:
                    anchoColumna = (6 * ancho) / 100;
                    break;
                case 2:
                    anchoColumna = (10 * ancho) / 100;
                    break;
                case 3:
                    anchoColumna = (23 * ancho) / 100;
                    break;
                case 4:
                    anchoColumna = (19 * ancho) / 100;
                    break;

            }
            columnaTabla.setPreferredWidth(anchoColumna);
        }
    }


