    private void btneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btneliminarActionPerformed
        if (codigo != 0) {
            opt = 'E';
            lblcod.setVisible(true);
            txtcodigo.setVisible(true);
            txtcodigo.setText("" + codigo);
            txtDNI.setText(DNI);
            txtRemitente.setText(Remitente);
            txtDireccion.setText(Direccion);
            txtTelefono.setText(Telefono);
            txtEmail.setText(Email);
           try {
                codigo = Integer.parseInt(txtcodigo.getText());
                
                if (opt == 'E') {
                    oRemitente = new Remitente();
                    oRemitente.setIdRemitente(codigo);
                    int rpta = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar",
                            "REMITENTE", JOptionPane.YES_NO_OPTION);
                    if (rpta == 0) {
                        if (RemitenteBo.eliminarRemitente(oRemitente)) {
                            JOptionPane.showMessageDialog(this, "Se Eliminó Correctamente", "REMITENTE",
                                    JOptionPane.INFORMATION_MESSAGE);
                            cargarDatos();
                        } else {
                            JOptionPane.showMessageDialog(this, "No se pudo Eliminar", "REMITENTE",
                                    JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "REMITENTE", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione el Remitente", "REMITENTE", JOptionPane.ERROR_MESSAGE);
        }
        btnsEdicionHabilitar();
        btnsConfirmarDeshabilitar();


    }//GEN-LAST:event_btneliminarActionPerformed

