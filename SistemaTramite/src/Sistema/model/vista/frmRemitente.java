    private void btngrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btngrabarActionPerformed
        DNI = txtDNI.getText();
        Remitente = txtRemitente.getText();
        Direccion = txtDireccion.getText();
        Telefono = txtTelefono.getText();
        Email = txtEmail.getText();
        oRemitente = new Remitente();
        switch (opt) {
            case 'N':
                oRemitente.setDNI(DNI);
                oRemitente.setRemitente(Remitente);
                oRemitente.setDireccion(Direccion);
                oRemitente.setTelefono(Telefono);
                oRemitente.setEmail(Email);
               
                try {
                    if (RemitenteBo.grabarRemitente(oRemitente)) {
                        JOptionPane.showMessageDialog(this, "Se Registró Correctamente", "MENSAJE --> REMITENTE", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Registrar", "MENSAJE --> REMITENTE", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "MENSAJE --> REMITENTE", JOptionPane.ERROR_MESSAGE);
                }
                break;
            case 'M':
                codigo = Integer.parseInt(txtcodigo.getText());
                oRemitente.setIdRemitente(codigo);
                oRemitente.setDNI(DNI);
                oRemitente.setRemitente(Remitente);
                oRemitente.setDireccion(Direccion);
                oRemitente.setTelefono(Telefono);
                oRemitente.setEmail(Email);
               
                try {
                    if (RemitenteBo.modificarRemitente(oRemitente)) {
                        JOptionPane.showMessageDialog(this, "Se Modificó Correctamente", "REMITENTE", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Modificar", "REMITENTE", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "REMITENTE", JOptionPane.ERROR_MESSAGE);
                }
                break;
        }
        camposDeshabilitado();
        lblcod.setVisible(false);
        txtcodigo.setVisible(false);
        cargarDatos();
        btnsConfirmarDeshabilitar();
        btnsEdicionHabilitar();


    }//GEN-LAST:event_btngrabarActionPerformed

