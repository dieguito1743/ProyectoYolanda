    public void setAnchoColumnas() {
        JViewport scroll = (JViewport) tblRemitente.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tblRemitente.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tblRemitente.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (4 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (6 * ancho) / 100;
                    break;
                case 2:
                    anchoColumna = (10 * ancho) / 100;
                    break;
                case 3:
                    anchoColumna = (23 * ancho) / 100;
                    break;
                     case 4:
                    anchoColumna = (23 * ancho) / 100;
                    break;
                     case 5:
                    anchoColumna = (23 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
        }
    }

