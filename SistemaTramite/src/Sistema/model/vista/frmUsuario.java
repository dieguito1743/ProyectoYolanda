    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
       Fecha = txtFecha.getText();
        Username = txtUsuario.getText();
        Pasword = txtClave.getText();
       
        oUsuario = new Usuario();
        switch (opt) {
            case 'N':
                oUsuario.setFecha(Fecha);
                oUsuario.setUsername(Username);
                oUsuario.setPasword(Pasword);
                try {
                    if (UsuarioBo.grabarUsuario(oUsuario)) {
                        JOptionPane.showMessageDialog(this, "Se RegistrÃ³ Correctamente", "MENSAJE --> USUARIO", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Registrar", "MENSAJE --> USUARIO", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "MENSAJE --> USUARIO", JOptionPane.ERROR_MESSAGE);
                }
                break;
            case 'M':
                Codigo = Integer.parseInt(txtCodigo.getText());
                oUsuario.setFecha(Fecha);
                oUsuario.setUsername(Username);
                oUsuario.setPasword(Pasword);
                try {
                    if (UsuarioBo.modificarUsuario(oUsuario)) {
                        JOptionPane.showMessageDialog(this, "Se ModificÃ³ Correctamente", "USUARIO", JOptionPane.INFORMATION_MESSAGE);
                        cargarDatos();
                    } else {
                        JOptionPane.showMessageDialog(this, "No se pudo Modificar", "USUARIO", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, e.getMessage(), "USUARIO", JOptionPane.ERROR_MESSAGE);
                }
                break;
        }
        camposDeshabilitado();
        lblCod.setVisible(false);
        txtCodigo.setVisible(false);
        cargarDatos();
        btnsConfirmarDeshabilitar();
        btnsEdicionHabilitar();
    }//GEN-LAST:event_btnGrabarActionPerformed

