    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (Codigo != 0) {
            opt = 'E';
            lblCod.setVisible(true);
            txtCodigo.setVisible(true);
            txtCodigo.setText("" + Codigo);
            txtFecha.setText(Fecha);
            txtUsuario.setText(Username);
            txtClave.setText(Pasword);
            try {
                int index = tblUsuario.getSelectedRow();
                Codigo = Integer.parseInt(tblUsuario.getValueAt(index, 0).toString());
                if (opt == 'E') {
                    Usuario oUsuario = new Usuario();
                    oUsuario.setIdUsuario(Codigo);
                    int rpta = JOptionPane.showConfirmDialog(this, "Seguro que desea eliminar", "Usuario", JOptionPane.YES_NO_OPTION);
                    if (rpta == 0) {
                        if (UsuarioBo.eliminarUsuario(oUsuario)) {
                            JOptionPane.showMessageDialog(this, "Se EliminÃ³ Correctamente", "USUARIO",
                                    JOptionPane.INFORMATION_MESSAGE);
                            cargarDatos();
                        } else {
                            JOptionPane.showMessageDialog(this, "No se pudo Eliminar", "USUARIO",
                                    JOptionPane.ERROR_MESSAGE);
                        }
                    }
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "USUARIO", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione el USUARIO", "USUARIO", JOptionPane.ERROR_MESSAGE);
        }
        btnsEdicionHabilitar();
        btnsConfirmarDeshabilitar();

}//GEN-LAST:event_btnEliminarActionPerformed

