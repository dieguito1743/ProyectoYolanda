    private void cargarDatos() {
        try {
            TableColumn columna;
            usuarioTableModel = UsuarioBo.obtenerListaUsuarios();
            tblUsuario.setModel(usuarioTableModel);
            setAnchoColumnas();
            columna = tblUsuario.getColumnModel().getColumn(0);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void setAnchoColumnas() {
        JViewport scroll = (JViewport) tblUsuario.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tblUsuario.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tblUsuario.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (10 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (35 * ancho) / 100;
                    break;
                case 2:
                    anchoColumna = (10 * ancho) / 100;
                    break;
                case 3:
                    anchoColumna = (15 * ancho) / 100;
                    break;
                case 4:
                    anchoColumna = (10 * ancho) / 100;
                    break;
                case 5:
                    anchoColumna = (10 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
        }
    }

