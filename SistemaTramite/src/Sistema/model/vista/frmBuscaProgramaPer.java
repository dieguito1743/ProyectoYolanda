    private void tblProgramaMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProgramaMousePressed
        if (evt.getClickCount() == 2) {
            int index = tblPrograma.getSelectedRow();
            permiso.getTxtPrograma().setText(tblPrograma.getValueAt(index, 1).toString());
            permiso.getTxtIdPrograma().setText(tblPrograma.getValueAt(index, 0).toString());
            this.dispose();
        }
    }//GEN-LAST:event_tblProgramaMousePressed

