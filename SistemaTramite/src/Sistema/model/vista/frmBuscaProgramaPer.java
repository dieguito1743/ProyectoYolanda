    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            TableColumn columna;
            docTableModel = ProgramaBo.obtenerListaProgramaBuscar(txtPrograma.getText());
            tblPrograma.setModel(docTableModel);
            setAnchoColumnas();
            columna = tblPrograma.getColumnModel().getColumn(0);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnBuscarActionPerformed
    public void setAnchoColumnas() {
        JViewport scroll = (JViewport) tblPrograma.getParent();
        int ancho = scroll.getWidth();
        int anchoColumna = 0;
        TableColumnModel modeloColumna = tblPrograma.getColumnModel();
        TableColumn columnaTabla;
        for (int i = 0; i < tblPrograma.getColumnCount(); i++) {
            columnaTabla = modeloColumna.getColumn(i);
            switch (i) {
                case 0:
                    anchoColumna = (7 * ancho) / 100;
                    break;
                case 1:
                    anchoColumna = (20 * ancho) / 100;
                    break;
                case 2:
                    anchoColumna = (15 * ancho) / 100;
                    break;
                case 3:
                    anchoColumna = (20 * ancho) / 100;
                    break;
            }
            columnaTabla.setPreferredWidth(anchoColumna);
        }
    }
